#include <opencv2/opencv.hpp>  
#include <opencv2/imgproc/imgproc.hpp>  


//-----------------------------------【命名空间声明部分】---------------------------------------  
//      描述：包含程序所使用的命名空间  
//-----------------------------------------------------------------------------------------------   
using namespace cv;


//-----------------------------------【main( )函数】--------------------------------------------  
//      描述：控制台应用程序的入口函数，我们的程序从这里开始  
//----------------------------------------------------------------------------------------------- 
int main()
{
	Mat src = imread("PIC.jpg");
	imshow("【原始图】", src);
	Rect ccomp;
	floodFill(src, Point(70, 89), Scalar(155, 255, 55), &ccomp, Scalar(5, 5, 5), Scalar(5, 5, 5));
	imshow("【效果图】", src);
	waitKey(0);

	return 0;
}
